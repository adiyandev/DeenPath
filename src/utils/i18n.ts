type Language = keyof typeof translations;
type TranslationKey = keyof typeof translations.en;

export function useTranslation() {
  let uiLang = 'en';
  if (typeof window !== 'undefined') {
    uiLang = window.localStorage.getItem('ui_language') || 'en';
  }
  
  if (!translations[uiLang as Language]) {
    uiLang = 'en';
  }

  const t = (key: TranslationKey) => {
    return translations[uiLang as Language][key] || translations.en[key] || key;
  };

  return { t, lang: uiLang };
}
